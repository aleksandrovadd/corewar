#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char a;

	a = c;
	if (!s)
		return (0);
	while (*s != a && *s != '\0')
		s++;
	if (*s == a)
		return ((char *)s);
	else
		return (0);
}
