#include "libft.h"

size_t	ft_strslen(char *s1, char *s2)
{
	size_t	i;
	size_t	ii;

	i = 0;
	ii = 0;
	if (*s1 && *s2)
	{
		while (s1[i])
			i++;
		while (s2[ii])
			ii++;
		return (i + ii);
	}
	return (0);
}
