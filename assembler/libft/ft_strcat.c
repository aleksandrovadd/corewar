#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*found;

	found = dest;
	while (*dest)
	{
		dest++;
	}
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (found);
}
