#include "../../include/vm.h"

int						read_visual(t_core *core, char **av, int *i)
{
	if (ft_strcmp(av[(*i)], "--visual") == 0)
	{
		FLAGS->visual = 1;
		++(*i);
		return (1);
	}
	return (0);
}
